package Proyecto2.Problema3;

import java.awt.Polygon;
import java.awt.Rectangle;

/**
 *
 * @author fbriceno
 */
public class Rectangulo  {

    int x;
    int y;
    int width ;
    int height;

    public Rectangulo(int x, int y, int width, int height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }

    
    public Rectangulo() {
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }
    
    
    
}
